    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        if (!text_user.getText().isBlank() && !text_senha.getText().isBlank() && !text_nome.getText().isBlank()) {
            try {
                boolean result = server.cadastrarUsuario(new User(0, text_user.getText(), text_senha.getText(), false, text_nome.getText()));
                if (result) {
                    JOptionPane.showMessageDialog(null, "Conta criada com sucesso!");
                    new Login(server).setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao criar conta");
                }
            } catch (RemoteException ex) {
                Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro ao criar conta: " + ex.getMessage());
            }

        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos");
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed

